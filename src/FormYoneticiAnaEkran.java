    private void btnDoktorGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoktorGuncelleActionPerformed
        int selectedRow=tabloDoktor.getSelectedRow();
        if(selectedRow==-1){
            if(model.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Doktor tablosu şu an boş.");
            }
            else{
                JOptionPane.showMessageDialog(this, "Lütfen güncellenecek bir doktor seçin");
            }
        }
        else{
            int id=(int) modelDoktor.getValueAt(selectedRow, 0);
            String k_ad=txtKullaniciAdi.getText();
            String sifre=txtSifre.getText();
            String tcNo=txtTcNo.getText();
            String ad=txtAd.getText();
            String soyad=txtSoyad.getText();
            String tel=txtTel.getText();
            String adres=txtAdres.getText();
            
            d.setId(id);
            d.setKullaniciAdi(k_ad);
            d.setSifre(sifre);
            d.setTcNo(tcNo);
            d.setAd(ad);
            d.setSoyad(soyad);
            d.setTel(tel);
            d.setAdres(adres);
            
            d.guncelle();
            doktorGoruntule();
        }
    }//GEN-LAST:event_btnDoktorGuncelleActionPerformed

