    public void doktorGoruntule(){
        modelDoktor.setRowCount(0);
        
        ArrayList<Doktor> kayitlar=new ArrayList<Doktor>();
        kayitlar=d.listele();
        if(kayitlar!=null){
            for(Doktor doktor: kayitlar)
            {
                Object [] eklenecek={doktor.getId(),doktor.getKullaniciAdi(),doktor.getSifre(),doktor.getTcNo(),doktor.getAd(),doktor.getSoyad(),doktor.getTel(),doktor.getAdres()};
                modelDoktor.addRow(eklenecek);
            }
        }
    }
    
    public void poliklinikGoruntule(){
        model.setRowCount(0);
        
        ArrayList<Poliklinikler> kayitlar=new ArrayList<Poliklinikler>();
        kayitlar=p.listele();
        if(kayitlar!=null){
            for(Poliklinikler poliklinik: kayitlar)
            {
                Object [] eklenecek={poliklinik.getId(),poliklinik.getAd()};
                model.addRow(eklenecek);
            }
        }
    }
