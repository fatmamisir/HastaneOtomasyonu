    private void btnAraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAraActionPerformed
        p.setId(Integer.valueOf(txtIdAra.getText()));
        p.ara();
        model.setRowCount(0);
        
        ArrayList<Poliklinikler> kayitlar=new ArrayList<Poliklinikler>();
        kayitlar=p.ara();
        if(kayitlar!=null){
            for(Poliklinikler poliklinik: kayitlar)
            {
                Object [] eklenecek={poliklinik.getId(),poliklinik.getAd()};
                model.addRow(eklenecek);
            }
        }
    }//GEN-LAST:event_btnAraActionPerformed

